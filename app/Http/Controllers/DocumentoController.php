    public static function moveFile($id,$old_path){
        $filename = explode('/', $old_path);
        $new_path = 'dispatch/'.$id.'/'.$filename[count($filename)-1];
        Storage::move($old_path, $new_path);
        Storage::delete($old_path);
        return $new_path;
    }

    public static function mimeType($path){
        if($path){
            $filename = explode('.', $path);
            $extension = strtolower(trim($filename[count($filename)-1]));
           return self::$types[$extension];
        }
        else
            return  '';
    }

    public function subir(Request $request)
    {
	    try
        {
		    if($request->hasFile('documento'))
            {
                $doc = $request->documento;
                $new_name = Str::uuid().'.' . $doc->getClientOriginalExtension();
                $result = $doc->storeAS('temp/'.Session::get('username'), $new_name);

                return response()->json([
		        	'documento' => $result
		     	]);
			}
		}
        catch(Exception $e)
        {
			return response()->json(['errors'=>$e->getMessage()]);
		}
	}

	public function descargar($id,$question){

        $result = RespuestaSolicitud::where('id_solicitud','=',$id)
                    ->where('id_pregunta','=',$question)
                    ->first();

        //return response()->json($result);
		if($result){
            $filename = explode('/', $result->ruta);

			return Storage::download($result->ruta,$filename[count($filename)-1]);
		}

	}

